extension StringUtils on String {
  /// Returns string with capitalized first letter
  ///
  /// Example:
  /// ```dart
  /// assert('test'.capitalizeFirstLetter(), 'Test');
  /// ```
  String capitalizeFirstLetter() => this.isNotEmpty
      ? '${this[0].toUpperCase()}${this.substring(1)}'
      : this;

  /// Returns true if string contains email
  bool get isEmail => _emailRegularExpression.hasMatch(this.toLowerCase());
}

/// Plural forms for russian words
/// [number] count quantity for word
String plural(int number, String form1, String form2, String form3) {
  final num = number % 100;

  if (num >= 11 && num <= 19) {
    return form3;
  }

  final i = num % 10;

  switch (i) {
    case 1:
      return form1;
    case 2:
    case 3:
    case 4:
      return form2;
    default:
      return form3;
  }
}
